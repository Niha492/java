package Operators;

public class Logical_operators {

	public static void main(String[] args) {
          int a=90;
          int b=120;
          int c=300;
          boolean d=(a<b) && (a>c);
          boolean e=(a<b) || (a>c);
          boolean f= !(a==c);
    System.out.println(d);
    System.out.println(e);
    System.out.println(f);
	}

}
